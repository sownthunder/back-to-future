// use this for initialization
function Start ()
{
	
	// set grid (array) to be 120x120
	grid = new GameObject[gridWidth, gridHeight];

	// Loop for the entire size of the array, 120x120, in this case
	for (var i = 0; i < gridWidth; i++)
	{
	
		for (var j = 0; j < gridHeight; j++)
		{
		
			// get the value of the particular tile within the array
			var tileValue = multiDimensionalArray[i][j];
			
			// If it is a border tile
			if (tileValue == 0)
			{
			
				//Debug.Log ("border");
				grid[i,j] = borderTile;
				Instantiate (grid[i,j], Vector3 (i, j, 0), Quaternion.identity);
			
			}
			// If it is a Grass tile 
			else if (tileValue == 1)
			{
			
				grid[i,j] = grassTile;
				Instantiate (grid[i,j], Vector3 (i, j, 0), Quaternion.identity);
			
			}
			// If it is a building tile
			else if (tileValue == 2)
			{
			
				grid[i,j] = buildingTile;
				Instantiate (grid[i,j], Vector3 (i, j, 0), Quaternion.identity);
			
			}
			// If it is a hazard spawner tile
			else if (tileValue == 3)
			{
			
				grid[i,j] = hazardSpawnerTile;
				Instantiate (grid[i,j], Vector3 (i, j, 0), Quaternion.identity);
			
			}
			// IF it is a vehicle spawner tile
			else if (tileValue == 4)
			{
			
				grid[i,j] = vehicleSpawnerTile;
				Instantiate (grid[i,j], Vector3 (i, j, 0), Quaternion.identity);
			
			}
			// If it is an enemy spawner tile
			else if (tileValue == 5)
			{
			
				grid[i,j] = enemySpawnerTile;
				Instantiate (grid[i,j], Vector3 (i, j, 0), Quaternion.identity);
			
			}
			// If it is an item spawner tile
			else if (tileValue == 6)
			{
			
				grid[i,j] = itemSpawnerTile;
				Instantiate (grid[i,j], Vector3 (i, j, 0), Quaternion.identity);
			
			}
			// If it is a railRoadTile
			else if (tileValue == 7)
			{
				
				grid[i,j] = railRoadTile;
				Instantiate (grid[i,j], Vector3 (i, j, 0), Quaternion.identity);
				
				// Push the position of the track onto the Array that the train object will follow
				TControllerScript.trainArr.Push (Vector3(i, j, -0.01));
			
			}
			// IF it is the inBoundRailTile
			else if (tileValue == 8)
			{
			
				//Debug.Log ("inBoundRail order-array: " + Vector3 (i, j, 0));
				
				/////////////////////////////
				
				////////////////////////////////////
				
				// spawn that shit and put it in grid
				grid[i,j] = inBoundRailTile;
				Instantiate (grid[i,j], Vector3 (i, j, 0), Quaternion.identity);
			
			}
			// If it is a road tile
			else if (tileValue == 9)
			{
			
				grid[i,j] = roadTile;
				Instantiate (grid[i,j], Vector3 (i, j, 0), Quaternion.identity);
			
			}
			
			
		}
	
	}

}


        //PControllerScript.transform.position = Vector3.Lerp(PControllerScript.transform.position, touchPos, (Time.deltaTime / 2));
        
        // **TESTING** the step size is equal to speed times frame time.
        var step = PControllerScript.speed * Time.deltaTime;

        // **TESTING** the vector3.MoveTowards
        PControllerScript.transform.position = Vector3.MoveTowards(PControllerScript.transform.position, touchPos, step);


        Debug.Log("<> ------------- <> IS MARTY McFLY LERPING DONE?!?!?!");

        // if MARTY's lerp-a-derp has brought him to the desired position (he has fulled lerped.... derp)
